/**
 * Get theme by name
 * @param {string} themeName - Theme name
 * @returns {object} - Theme colors object
 */
export const getThemeByName = (themeName) => {
  return allThemes.find(theme => theme.name.toLowerCase() === themeName.toLowerCase())
}

/**
 * Get theme by index
 * @param {number} index - Theme index (0-9)
 * @returns {object} - Theme colors object
 */
export const getThemeByIndex = (index) => {
  return allThemes[index] || allThemes[0]
}

// ==========================================
// Recommended Defaults
// ==========================================
export const defaultTheme = theme1_GreenTeal

/**
 * Format theme for database storage
 * @param {object} theme - Theme object
 * @returns {object} - Formatted for database
 */
export const formatThemeForDatabase = (theme) => {
  return {
    ...theme.colors,
    themeName: theme.name,
    themeDescription: theme.description
  }
}

/**
 * Get all theme names for selection
 * @returns {array} - Array of theme names
 */
export const getThemeNames = () => {
  return allThemes.map(theme => theme.name)
}

/**
 * Get random theme
 * @returns {object} - Random theme
 */
export const getRandomTheme = () => {
  const randomIndex = Math.floor(Math.random() * allThemes.length)
  return allThemes[randomIndex]
}
